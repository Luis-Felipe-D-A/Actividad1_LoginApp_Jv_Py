    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
                                        
    Usuarios u = new Usuarios();

    if(txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtEmail.getText().isEmpty()
       || txtUsername.getText().isEmpty() || txtClave.getPassword().length == 0) {
        JOptionPane.showMessageDialog(this, "completar todos los campos");
        return;
    }

    boolean actualizado = u.actualizarUsuario( txtNombre.getText(), txtApellido.getText(),txtEmail.getText(), txtUsername.getText(),
        new String(txtClave.getPassword()),
        cmbRol.getSelectedItem().toString()
    );

    if (actualizado) {
        cargarTabla();  
        limpiarCampos();
    }
}

private void limpiarCampos() { txtNombre.setText(""); txtApellido.setText(""); txtEmail.setText(""); txtUsername.setText(""); txtClave.setText("");  cmbRol.setSelectedIndex(0);


    }//GEN-LAST:event_btnEditarActionPerformed

